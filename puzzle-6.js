function parseInput(input) {
  const data = input.split("\n")
  return function (x, y) {
    const row = data[y]
    let cells = row.split("");
    return cells[x % cells.length]
  }
}

let getter = parseInput(input);

function solve(right, down) {
  let x = right;
  let y = down;
  let trees = 0
  while (y < input.split("\n").length) {
    if (getter(x, y) === "#") {
      trees++
    }
    x += right
    y += down
  }
  return trees
}

